// While Loop.
// suppose if we want to print number from 0 to 9.

let i = 0;

while(i<=9){
  console.log(i);
  i++;
}
console.log(`Current Value of i is : ${i}`);

let num = 10;
let j=1;
let sum=0;

while(j<=num){
  sum = sum + j;
  j++;
}
console.log(`Sum of first ${num} natural number is ${sum}`);
