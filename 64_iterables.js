// iterables
// on which we can apply for of loop.
// string, array are iterables.

// String is iterable becoz we can apply for of loop on it
  const firstName = "Dev";
  for(let char of firstName){
    console.log(char);
  }

// array is iterable becoz we can apply for of loop on it.
  const fruits = ["mango", "bannana", "orange", "grapes", "kiwi"];
  for(let fruit of fruits){
    console.log(fruit);
  }
